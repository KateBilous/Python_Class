# Найти результат выражения для произвольных значений a,b,c: ( ln( 1 + c ) / -b )4+ | a |

import math

a = 5
b = 8
c = 16


d = pow((math.log(1 + c) / -b), 4) + abs(a)

print(" Результат выражения для произвольных значений a,b,c: ( ln( 1 + c ) / -b )4+ | a | = ", d)

#------------------or-----------------------------------

z = ((math.log(1 + c) / -b) ** 4) + abs(a)
print(" Результат выражения для произвольных значений a,b,c: ( ln( 1 + c ) / -b )4+ | a | = ", z)